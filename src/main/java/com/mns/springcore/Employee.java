package com.mns.springcore;

public class Employee {
    private int id;
    private String name;
    public String company;

    Employee(String company) {
        this.company = company;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
